// Content plan generator based on posts per week (3, 5, 7)
export function generateContentPlan(subNiche: string, postsPerWeek: number): ContentPlanDay[] {
  const rubrics = getRubricsForNiche(subNiche);
  const plan: ContentPlanDay[] = [];
  const daysOfWeek = ['Понедельник', 'Вторник', 'Среда', 'Четверг', 'Пятница', 'Суббота', 'Воскресенье'];

  // Map of post types to rubrics for rotation
  const rubricsRotation = [
    { rubric: rubrics[0].title, topic: 'Топ-3 лайфхака для решения острой проблемы в вашей нише', type: 'expert' },
    { rubric: rubrics[3].title, topic: 'Личный инсайт: как я совершил ошибку и какой вывод сделал', type: 'story' },
    { rubric: rubrics[1].title, topic: 'Кейс: как клиент добился крутого результата вашим методом', type: 'case' },
    { rubric: rubrics[2].title, topic: 'Главная ошибка, которую делают 90% новичков при попытке решить проблему', type: 'error_myth' },
    { rubric: rubrics[6].title, topic: 'Специальное предложение: запись на вашу ключевую услугу', type: 'sales' },
    { rubric: rubrics[4].title, topic: 'Интерактив: отвечаю на самый неудобный вопрос подписчиков', type: 'question' },
    { rubric: rubrics[7].title, topic: 'Разрушаем миф, который мешает вам двигаться дальше', type: 'error_myth' }
  ];

  let rotationIndex = 0;

  // Generate 30 days
  for (let day = 1; day <= 30; day++) {
    const dayOfWeekIndex = (day - 1) % 7;
    const dayOfWeek = daysOfWeek[dayOfWeekIndex];

    // Determine if we publish on this day based on posts per week
    let shouldPublish = false;
    if (postsPerWeek === 7) {
      shouldPublish = true;
    } else if (postsPerWeek === 5) {
      // Publish Mon, Tue, Wed, Fri, Sat
      shouldPublish = [0, 1, 2, 4, 5].includes(dayOfWeekIndex);
    } else {
      // 3 posts per week: Mon, Wed, Fri
      shouldPublish = [0, 2, 4].includes(dayOfWeekIndex);
    }

    if (shouldPublish) {
      const currentItem = rubricsRotation[rotationIndex % rubricsRotation.length];
      plan.push({
        dayNumber: day,
        dayOfWeek,
        rubric: currentItem.rubric,
        topic: currentItem.topic,
        postType: currentItem.type,
        published: false
      });
      rotationIndex++;
    } else {
      // Non-publishing day, can be for Stories only or rest
      plan.push({
        dayNumber: day,
        dayOfWeek,
        rubric: '☕ День отдыха / Сторис',
        topic: 'Опубликуйте легкие интерактивные разговорные сторис из вашей жизни без постов.',
        postType: 'stories_only',
        published: false
      });
    }
  }

  return plan;
}

