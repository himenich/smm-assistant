// Hashtag generator
export function getHashtagsForNiche(subNiche: string, userName: string = 'Эксперт'): HashtagGroup {
  const ctx = getNicheContext(subNiche);
  const nameSlug = userName.replace(/\s+/g, '');
  const cleanKeywords = ctx.keywords.map(k => k.toLowerCase());

  return {
    author: [
      `#${nameSlug}Рекомендует`,
      `#${nameSlug}Делится`,
      `#${nameSlug}Кейс`,
      `#Будни${nameSlug}`
    ],
    thematic: [
      `#${cleanKeywords[0] || 'продвижение'}`,
      `#${cleanKeywords[1] || 'смм'}`,
      `#${cleanKeywords[2] || 'маркетинг'}`,
      `#${cleanKeywords[3] || 'советы'}`,
      `#польза${cleanKeywords[0] || 'бизнеса'}`
    ],
    navigation: [
      '#полезно',
      '#кейсы',
      '#лайфхак',
      '#ответынавопросы',
      '#обо мне'
    ]
  };
}

